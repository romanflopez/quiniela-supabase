import { scrapearTodasJurisdicciones } from './lib/scraper-core.js';
import { guardarResultados, closeDB } from './lib/database.js';
import { getTodayDateArg, getTurnoFromId, log } from './lib/utils.js';

const SORTEO_ID = process.argv[2];
const FECHA = process.argv[3] || getTodayDateArg();

if (!SORTEO_ID) {
    console.error('‚ùå Uso: node scripts/scraper-by-sorteo-id.js [SORTEO_ID] [FECHA_OPCIONAL]');
    console.error('Ejemplo: node scripts/scraper-by-sorteo-id.js 51774 2025-12-12');
    process.exit(1);
}

async function main() {
    const startTime = Date.now();
    
    log('üéØ', `‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê`);
    log('üéØ', `SCRAPER BY ID - Sorteo: ${SORTEO_ID}`);
    log('üéØ', `Fecha: ${FECHA} | Turno: ${getTurnoFromId(SORTEO_ID)}`);
    log('üéØ', `‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê`);
    
    try {
        // Scrapear todas las jurisdicciones para este sorteo
        const resultados = await scrapearTodasJurisdicciones(SORTEO_ID, FECHA, 3000);
        
        const totalTime = Math.round((Date.now() - startTime) / 1000);
        
        if (resultados.length === 0) {
            log('‚ö†Ô∏è', 'No se encontraron datos para este sorteo');
            process.exit(1);
        }
        
        // Guardar en DB
        const stats = await guardarResultados(resultados);
        
        log('üéâ', `‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê`);
        log('üéâ', `COMPLETADO en ${totalTime}s`);
        log('üéâ', `Resultados: ${resultados.length}/4 jurisdicciones`);
        log('üéâ', `Guardados: ${stats.guardados} | Errores: ${stats.errores}`);
        log('üéâ', `‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê`);
        
        // Retornar JSON para consumo de la app
        console.log('\nüìÑ JSON OUTPUT:');
        console.log(JSON.stringify({
            success: true,
            sorteo_id: SORTEO_ID,
            fecha: FECHA,
            turno: getTurnoFromId(SORTEO_ID),
            resultados: resultados.map(r => ({
                jurisdiccion: r.jurisdiccion,
                cabeza: r.cabeza,
                numeros: r.numeros,
                letras: r.letras
            })),
            total: resultados.length,
            tiempo_segundos: totalTime
        }, null, 2));
        
        process.exit(0);
        
    } catch (error) {
        log('‚ùå', `Error fatal: ${error.message}`);
        console.error(error);
        process.exit(1);
        
    } finally {
        await closeDB();
    }
}

main();


